import mongoose from "mongoose";
const UserModelScheema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  modifiedAt: {
    type: Date,
    default: Date.now,
  },
});
const UserModel =
  mongoose.models.user || mongoose.model("user", UserModelScheema);
export default UserModel;
